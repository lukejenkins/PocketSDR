/*
 *  Free spreading (PRN) code.
 *  
 *  args:
 *      code     (I)  spreading (PRN) code
 *
 *  return:
 *      none
 *
 */
void sdr_code_free(sdr_code_t *code)
{
    if (!code) return;
    sdr_free(code->code);
    sdr_free(code->code_s);
    sdr_free(code);
}

