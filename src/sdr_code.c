/* constants and macros ------------------------------------------------------*/
#define CODE_LEN_L1CA     1023        /* L1C/A code length (chip) */
#define CODE_LEN_L5Q      10230       /* L5Q primary code length (chip) */
#define CODE_LEN_L5QS     20          /* L5Q secondary code length (chip) */
#define CODE_LEN_L6       20460       /* L6 code length (chip) */
#define CODE_LEN_E1B      4092        /* E1B code length (chip) */
#define CODE_LEN_E6B      5115        /* E6B code length (chip) */

