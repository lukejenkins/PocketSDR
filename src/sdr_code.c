/* genrate L1C/A code ([1] 3.3.2.3) ------------------------------------------*/
static sdr_code_t *gen_code_L1CA(int prn)
{
    sdr_code_t *code;
    int i, j, N = CODE_LEN_L1CA;
    int8_t G1[N], G2[N], R1[10], R2[10], R10, R20;
    
    if (prn < 1 || prn > 210) {
        return NULL;
    }
    code = (sdr_code_t *)sdr_malloc(sizeof(sdr_code_t));
    code->code = (int8_t *)sdr_malloc(N);
    
    for (i = 0; i < 10; i++) {
        R1[i] = R2[i] = -1;
    }
    for (i = 0; i < N; i++) {
        G1[i] = R1[9];
        G2[i] = R2[9];
        R10 = R1[2] * R1[9];
        R20 = R2[1] * R2[2] * R2[5] * R2[7] * R2[8] * R2[9];
        memmove(R1 + 1, R1, 9);
        memmove(R2 + 1, R2, 9);
        R1[0] = R10;
        R2[0] = R20;
    }
    for (i = 0, j = N - L1CA_G2_delay[prn - 1]; i < N; i++, j++) {
        code->code[i] = -G1[i] * G2[j % N];
    }
    code->len = N;
    code->rate = N / CODE_PER_L1CA;
    return code;
}

/* generate L5Q code ([2] 3.3.2.2) -------------------------------------------*/
static sdr_code_t *gen_code_L5Q(int prn)
{
    sdr_code_t *code;
    int i, j, N = CODE_LEN_L5Q, M = CODE_LEN_L5QS;
    int8_t XA[8190], XB[8191], RA[13], RB[13], RA0, RB0;
    
    if (prn < 1 || prn > 210) {
        return NULL;
    }
    code = (sdr_code_t *)sdr_malloc(sizeof(sdr_code_t));
    code->code = (int8_t *)sdr_malloc(N);
    code->code_s = (int8_t *)sdr_malloc(M);
    
    for (i = 0; i < 13; i++) {
        RA[i] = RB[i] = -1;
    }
    for (i = 0; i < 8190; i++) {
        XA[i] = RA[12];
        XB[i] = RB[12];
        RA0 = RA[8] * RA[9] * RA[11] * RA[12];
        RB0 = RB[0] * RB[2] * RB[3] * RB[5] * RB[6] * RB[7] * RB[11] * RB[12];
        memmove(RA + 1, RA, 12);
        memmove(RB + 1, RB, 12);
        RA[0] = RA0;
        RB[0] = RB0;
    }
    XB[i] = RB[12];
    
    for (i = 0, j = L5Q_XB_adv[prn - 1]; i < N; i++, j++) {
        code->code[i] = -XA[i % 8190] * XB[j % 8191];
    }
    for (i = 0; i < M; i++) {
        code->code_s[i] = NH20[i] ? 1 : -1;
    }
    code->len = N;
    code->rate = N / CODE_PER_L5Q;
    code->len_s = M;
    code->rate_s = M / CODE_PER_L5QS;
    return code;
}

/* generate L6 code ([4] 3.2.2) ----------------------------------------------*/
static sdr_code_t *gen_code_L6(int prn)
{
    sdr_code_t *code;
    int i, N = CODE_LEN_L6;
    uint8_t R1[10], R2[20], R10, R20, code_[N / 2];
    
    if (prn < 193 || prn > 211 || prn == 198 || prn == 202 || prn == 208) {
        return NULL;
    }
    code = (sdr_code_t *)sdr_malloc(sizeof(sdr_code_t));
    code->code = (int8_t *)sdr_malloc(N);
    
    for (i = 0; i < 10; i++) {
        R1[i] = 1;
    }
    for (i = 0; i < 20; i++) {
        R2[i] = (uint8_t)((L6_init_SF[prn - 193] >> i) & 1);
    }
    for (i = 0; i < N / 2; i++) {
        code_[i] = R1[9] ^ R2[19];
        R10 = R1[ 9] ^ R1[ 8] ^ R1[ 5] ^ R1[ 4] ^ R1[3] ^ R1[2];
        R20 = R2[19] ^ R2[18] ^ R2[15] ^ R2[13];
        memmove(R1 + 1, R1,  9);
        memmove(R2 + 1, R2, 19);
        R1[0] = R10;
        R2[0] = R20;
    }
    for (i = 0; i < N; i++) {
        if (prn <= 202) {
            code->code[i] = (i % 2) ? 0 : (code_[i / 2] ? 1 : -1);
        }
        else {
            code->code[i] = (i % 2) ? (code_[i / 2] ? 1 : -1) : 0;
        }
    }
    code->len = N;
    code->rate = N / CODE_PER_L6;
    return code;
}

/* genrate E1B code ([5] Annex C.7) ------------------------------------------*/
static sdr_code_t *gen_code_E1B(int prn)
{
    sdr_code_t *code;
    uint32_t hex;
    int i, N = CODE_LEN_E1B;
    
    if (prn < 1 || prn > 36) {
        return NULL;
    }
    code = (sdr_code_t *)sdr_malloc(sizeof(sdr_code_t));
    code->code = (int8_t *)sdr_malloc(N);
    
    for (i = 0; i < N; i++) {
        if (sscanf(E1B_code_hex[prn - 1] + i / 4, "%1X", &hex) == 1) {
            code->code[i] = ((hex >> (3 - i % 4)) & 1) ? 1 : -1;
        }
    }
    code->len = N;
    code->rate = N / CODE_PER_E1B;
    return code;
}

