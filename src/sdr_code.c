/*---------------------------------------------------------------------------*/
/*
 *  Genrate spreading (PRN) code.
 *  
 *  args:
 *      code     (I)  code type (SDR_CODE_???)
 *      prn      (I)  PRN number
 *
 *  return:
 *      spreading (PRN) code (NULL: error)
 */
sdr_code_t *sdr_code_gen(int code, int prn)
{
    switch (code) {
        case SDR_CODE_L1CA: return gen_code_L1CA(prn);
        case SDR_CODE_L5Q : return gen_code_L5Q (prn);
        case SDR_CODE_L6  : return gen_code_L6  (prn);
        case SDR_CODE_E1B : return gen_code_E1B (prn);
        case SDR_CODE_E5AQ: return gen_code_E5AQ(prn);
        case SDR_CODE_E6B : return gen_code_E6B (prn);
    }
    return NULL;
}

