// generate LDPC parity check matrix --------------------------------------------
static void *gen_LDPC_H(int m, int n, const uint16_t H_A[][2],
    const uint16_t H_B[][2], const uint16_t H_C[][2], const uint16_t H_D[][2],
    const uint16_t H_E[][2], const uint16_t H_T[][2], int na, int nb, int nc,
    int nd, int ne, int nt)
{
    void *H = mod2sparse_allocate(m, n);
    
    for (int i = 0; i < na; i++) {
        mod2sparse_insert(H, H_A[i][0] - 1, H_A[i][1] - 1);
    }
    for (int i = 0; i < nb; i++) {
        mod2sparse_insert(H, H_B[i][0] - 1, H_B[i][1] - 1 + m);
    }
    for (int i = 0; i < nc; i++) {
        mod2sparse_insert(H, H_C[i][0] - 2 + m, H_C[i][1] - 1);
    }
    for (int i = 0; i < nd; i++) {
        mod2sparse_insert(H, H_D[i][0] - 2 + m, H_D[i][1] - 1 + m);
    }
    for (int i = 0; i < ne; i++) {
        mod2sparse_insert(H, H_E[i][0] - 2 + m, H_E[i][1] + m);
    }
    for (int i = 0; i < nt; i++) {
        mod2sparse_insert(H, H_T[i][0] - 1, H_T[i][1] + m);
    }
    return H;
}

// free LDPC parity check matrix ------------------------------------------------
static void free_LDPC_H(void *H)
{
    mod2sparse_free(H);
}

// decode LDPC with parity check matrix -----------------------------------------
static void decode_LDPC_H(void *H, int m, int n, const uint8_t *syms,
    uint8_t *syms_dec)
{
    double *lratio = (double *)sdr_malloc(sizeof(double) * n);
    char   *dblk   = (char   *)sdr_malloc(sizeof(char)   * n);
    char   *pchk   = (char   *)sdr_malloc(sizeof(char)   * n);
    double *bitpr  = (double *)sdr_malloc(sizeof(double) * n);
    
    for (int i = 0; i < n; i++) {
        lratio[i] = RATIO[syms[i]];
    }
    // decode LDPC by probability propagation
    unsigned int iters = prprp_decode(H, lratio, dblk, pchk, bitpr);
    
    //int valid = check(H, dblk, pch) == 0
    //int chngd = changed(lratio, dblk, pchk, n);
    //fprintf(stderr, "decode_LDPC_H: iters=%d valid=%d chngd=%d\n", iters,
    //    valid, chngd);
    
    memcpy(syms_dec, dblk, m);
}

